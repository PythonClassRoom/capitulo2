# creacion de lista para hacer pruebas
mi_lista = ['pera', 'banano', 'piña', 'manzana']

# forma de llamar los elementos de la lista en reversa
['manzana', 'piña', 'banano', 'pera'] == mi_lista[::-1]

#forma de seleccionar los elementos de indice par
['pera', 'piña'] == mi_lista[::2]

#forma de seleccionar los elementos de indice impar
['pera', 'piña'] == mi_lista[1::2]

#sublista en reversa
['pera', 'banano', 'piña'] == mi_lista[-5:-1]